time = input()
#nhập thời gian
if time =='5 AM':
#nếu là 5 giờ sáng
    print("Wake up")
elif time =='6 AM':
#nếu không phải là 5 AM THÌ MỚI CHẠY
    print("Yoga")
elif time =='7 AM':
#nếu không phải là 6 AM THÌ MỚI CHẠY    
    print("Work")
else:
#nếu không phải các trường hợp trên
    print("Do something else")

